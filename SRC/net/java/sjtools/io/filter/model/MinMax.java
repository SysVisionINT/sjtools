package net.java.sjtools.io.filter.model;

import java.io.Serializable;

public class MinMax implements Serializable {

	private static final long serialVersionUID = -6082201288975596366L;

	private long min = 0;
	private long max = 0;

	public MinMax() {}

	public MinMax(long min, long max) {
		this.min = min;
		this.max = max;
	}

	public long getMin() {
		return min;
	}

	public void setMin(long min) {
		this.min = min;
	}

	public long getMax() {
		return max;
	}

	public void setMax(long max) {
		this.max = max;
	}

}
